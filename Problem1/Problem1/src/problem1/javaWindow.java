    /**
     * Checks if binary or not
     *
     * @param input
     * @return boolean
     */
    public static boolean isBinary(String input) {
        for (int i = 0; i < input.length(); i++) {
            if (input.charAt(i) != '0' || input.charAt(i) != '1') {
                return false;
            }
        }
        return true;
    }
