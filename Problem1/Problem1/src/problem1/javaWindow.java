    /**
     * When clicked it will convert binary to decimal
     *
     * @param evt
     */
    private void convertToDecimalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_convertToDecimalActionPerformed

        try {
            if (!isBinary(binaryInput.getText())) {
                Toolkit.getDefaultToolkit().beep();
                decimalOutput.setText("Invalid input");
            } else {
                int input = Integer.parseInt(binaryInput.getText());
                decimalOutput.setText(convert.toDecimal(input));
            }
        } catch (NumberFormatException nfe) {
            Toolkit.getDefaultToolkit().beep();
            decimalOutput.setText("Invalid input");
            nfe.printStackTrace(System.out);
        }
    }//GEN-LAST:event_convertToDecimalActionPerformed

