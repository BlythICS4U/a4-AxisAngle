/**
     * When clicked it will convert decimal to binary
     *
     * @param evt
     */
    private void convertToBinaryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_convertToBinaryActionPerformed
        try {
            int input = Integer.parseInt(decimalInput.getText());
            if (input < 0 || input > 255) {
                Toolkit.getDefaultToolkit().beep();
                binaryOutput.setText("Must be between 0 and 255");
            } else {
                binaryOutput.setText(convert.toBinary(input));
            }
        } catch (NumberFormatException nfe) {
            Toolkit.getDefaultToolkit().beep();
            binaryOutput.setText("Invalid input");
            nfe.printStackTrace(System.out);
        }
    }//GEN-LAST:event_convertToBinaryActionPerformed

