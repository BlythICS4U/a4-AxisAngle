    private void submitButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submitButtonMouseClicked
        String filename = "results.txt";
        file.create(filename); //Creates a new file
        
        //Retrieves data from the current file
        int surveyCount = retrieve.surveyCount(filename);
        int[] question1 = retrieve.question1Data(filename);
        int[] question2 = retrieve.question2Data(filename); 
        String[] question3 = retrieve.question3Data(filename, surveyCount);
        
        //Updates the file
        file.update(filename, surveyCount, question1, question2, question3, getCheckBox(), getComboBox(), getTextArea());
    }//GEN-LAST:event_submitButtonMouseClicked

