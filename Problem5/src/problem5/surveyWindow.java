    /**
     *  Reads the checkboxes
     * @return  int array filled with 0s or 1s
     */
    public int[] getCheckBox() {
        boolean[] values = new boolean[9];

        values[0] = checkBoxShooters.isSelected();
        values[1] = checkBoxFighters.isSelected();
        values[2] = checkBoxPlatformers.isSelected();

        values[3] = checkBoxStrategy.isSelected();
        values[4] = checkBoxRPG.isSelected();
        values[5] = checkBoxPuzzle.isSelected();

        values[6] = checkBoxArcade.isSelected();
        values[7] = checkBoxSports.isSelected();
        values[8] = checkBoxSports.isSelected();

        int[] intValues = new int[9];
        for (int i = 0; i < values.length; i++) {
            if (values[i]) {
                intValues[i] = 1;
            } else {
                intValues[i] = 0;
            }
        }
        return intValues;
    }

    /**
     * Checks which box is selected
     *
     * @return Returns an array with integers
     */
    public int[] getComboBox() {
        int[] values = new int[4];
        Arrays.fill(values, 0);

        if (comboBoxHours.getSelectedItem().equals("< 5 hours")) {
            values[0] = 1;
        } 

        if (comboBoxHours.getSelectedItem().equals("< 10 hours")) {
            values[1] = 1;
        }
        
        if (comboBoxHours.getSelectedItem().equals("< 15 hours")) {
            values[2] = 1;
        }
        
        if (comboBoxHours.getSelectedItem().equals("15+ hours")) {
            values[3] = 1;
        } 
        return values;
    }

